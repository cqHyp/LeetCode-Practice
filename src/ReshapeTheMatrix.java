public class ReshapeTheMatrix {
    public int[][] matrixReshape(int[][] nums, int r, int c) {
        int n = nums.length;
        int m = nums[0].length;
        int[][] res = new int[99][99];
        if (r * c != n * m) return nums;
        return res;
    }
}
